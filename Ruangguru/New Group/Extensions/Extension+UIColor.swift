import Foundation
import UIKit

extension UIColor {
	
	@nonobjc class var purple: UIColor {
		return UIColor(red: 144.0 / 255.0, green: 19.0 / 255.0, blue: 254.0 / 255.0, alpha: 1.0)
	}
	
	@nonobjc class var coral: UIColor {
		return UIColor(red: 251 / 255.0, green: 64 / 255.0, blue: 64 / 255.0, alpha: 1.0)
	}
	
	@nonobjc class var aquaMarine: UIColor {
		return UIColor(red: 80 / 255.0, green: 227 / 255.0, blue: 194 / 255.0, alpha: 1.0)
	}
}
